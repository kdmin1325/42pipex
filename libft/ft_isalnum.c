#include "libft.h"

int	ft_isalnum(int c)
{
	if (64 < c)
		if (c < 91)
			return (1);
	if (96 < c)
		if (c < 123)
			return (1);
	if (47 < c)
		if (c < 58)
			return (1);
	return (0);
}
